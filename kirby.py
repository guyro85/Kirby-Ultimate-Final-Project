def drawField(num, win, life):
    """
    the function draws the field according to the number
    :param num: the number of the field
    :param win: the window
    :param life: the life of the character
    """
    if num == 0:
        win.blit(field0, (0, 0))

    elif num == 1:
        win.blit(field1, (0, 0))

    elif num == 2:
        win.blit(field2, (0, 0))

    pos_x = 380
    pos_y = 560

    for x in range(0, life):
        win.blit(LIFE, (pos_x, pos_y))
        pos_x += 10


