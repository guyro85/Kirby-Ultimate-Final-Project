def kirby(x, y, img, win):
    "the function displays the image of kirby scaled 1.5x"
    scaled_img = pygame.transform.scale(img, (int(img.get_width() * 1.5), int(img.get_height() * 1.5)))
    win.blit(scaled_img, (x, y))
    pygame.display.update()


